package ru.ftob.grostore.rest.webmodel;

public class GuiAccount {
    private int id;
    private String name;

    public GuiAccount() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
